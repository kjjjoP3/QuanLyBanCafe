 public void init(){
        setLocationRelativeTo(null);
        //thread là 1 khỗi mã thực hiện nhiệm vụ
        Thread t=new Thread(){
                int i=-1;
          @Override
          public void run(){
              while(true){
                  try {
                      i++;
                      prg.setValue(i);
                      if(i==20)lblStatus.setText("Đang khởi tạo cái modun...");
                      if(i==50)lblStatus.setText("Đang kết nối CSDL...");
                      if(i==90)lblStatus.setText("Chuẩn bị vào chương trình...");
                      if(i==100){
                         JfrChao.this.dispose();   //đóng JfrChao
                         LoginFrNew dialog = new LoginFrNew(); // gọi đăng nhập
                         dialog.setVisible(true); // mở đăng nhập sau khi đóng
                      }
                      Thread.sleep(25);   //thread tạm dừng hoạt động trong 25 ms
                      value.setText(i + "%"); //hiển thị % load ms
                 } catch (InterruptedException ex) {
                      break;
                  }
              }
          }  
        };
        t.start();       //thread bắt đầu hoạt động
    }

